void main(List<String> args) {
  int yas = 21;
  print(yas);
  yas = 22;
  print(yas);

  // num ile double arasında ufak bir fark vardır.
  // num'a tam sayılı bir değer ataması yaparsan o konsola gördüğünü yazar.
  // fakat double'a tam sayılı bir değer atarsan sonuna her şekilde .0 koyar.

  num beyzaKilo = 60;
  print(beyzaKilo);

  double beyzaBoy = 173;
  print(beyzaBoy);

  // boolean true/false değerleri alır.

  bool kedisiVarMi = false;
  print(kedisiVarMi);
}
